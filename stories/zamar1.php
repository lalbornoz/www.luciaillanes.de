<h1 class="text-centre">Zamār atarḥáyyim amurrim rāšim libbíšu</h1>
<h2 class="text-centre">Lay of Atarḥáyyu the Amorite, the Hero</h2>
<h3 class="text-centre">I</h3>

<p class="text-green text-centre">
Once, there was a story<br>
in winding words lay written<br>
of all that was to end<br>
and emptiness appeared<br>
</p>

<p class="text-red text-centre">
When Atarḥáyyu rāš Libbíšu<br>
the Hero, the Possessor of his Heart,<br>
drew forth afore the Nothing<br>
set out to find a name<br>
on darkest trails of darkness<br>
for none he shall obtain<br>
<br>
He bore the noble Splendour<br>
and laid his arms to rest<br>
abandoned hope and gain<br>
nor time should him attain<br>
<br>
He cast aside his sight<br>
for naught he was to yearn<br>
when calmness reigned his eyes<br>
to emptiness he turned<br>
<br>
He rode a seven saroi<br>
and seven šār he rode<br>
on darkest trails of darkness<br>
his destiny he wrote<br>
<br>
He drove a road that went<br>
where beginning came to end<br>
where ending was unknown<br>
and all was drawn anew<br>
</p>

<p class="text-green text-centre">
Across lands spread far and wide<br>
the Splendour was his guide<br>
to mountains laid on high<br>
to the stars and to the sky<br>
he went but did not find<br>
<br>
Across rivers did he sail<br>
the Splendour his avail<br>
to the corners of the world<br>
to Šubartu, Martu, Elamtu, Šumer<br>
the weight of all he was to bear<br>
<br>
Across promontory and vale<br>
the Splendour cast his way<br>
to the Nothingness, the Pale<br>
to the darkness, to the Nothing<br>
a name he came to find<br>
</p>

<p class="text-red text-centre">
He drew forth afore the Pale<br>
set out to find a name<br>
to the Far-Away, Remote<br>
to Ūtnápištim he came<br>
<br>
A name he was to lend<br>
to Ningídumu, the Kindly,<br>
to Īnkuzén, the Golden-Eyed,<br>
when all was but to end<br>
<br>
To Ūtnápištim he came<br>
when all was but to end<br>
the Far-Away, Remote<br>
when naught there was of hope<br>
<br>
When Atarḥáyyu, the Hero<br>
drew forth to find a name<br>
may blessed be his hands<br>
may the Splendour be his countenance<br>
may calmness reign his eyes<br>
</p>

<p class="text-green text-centre">
Once, there was a story<br>
in silent signs concealed<br>
it drew itself anew<br>
and emptiness drew near<br>
</p>

