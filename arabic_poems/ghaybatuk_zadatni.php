<h2 class="text-centre">Ġaybatuk zādatnī ašwāq</h2>

<p class="text-centre text-italic">
ﻧﻮﻉ ﺍﻟﺸﻌﺮ: صنعة زجل; صنعات تواشيح<br>
ﺍﻟﺸﺎﻋﺮ: ﻣﺠﻬﻮﻝ؛ ﻣﻮﺟﻮﺩ ﻓﻲ ﻣﻴﺰﺍﻥ قدام الحجاز المشرقي<br>
منطقته: ﺍﻷﻧﺪﻟﺲ<br>
ﻋﺼﺮ: ﻗﺮﻥ ﺍﻟﺜﺎﻣﻦ-ﺍﻟﺨﺎﻣﺲ ﻋﺸﺮ<br>
</p>

