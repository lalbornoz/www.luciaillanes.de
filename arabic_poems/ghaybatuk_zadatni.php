<tr><td colspan="2" class="text-bold text-italic">صنعة زجل توشية</td></tr>
<tr><td colspan="2"><br></td></tr>
<tr>
<td>غَيبَتُكْ زَادَتْنِي أشْوَاقْ</td>
<td>وَالمَنَامْ رِيتُو جَفَانِي</td>
</tr>
<tr>
<td>لَمْ يَزَلْ طَبْعِي رَقِيقْ</td>
<td>حَتَّى صَارْ بِالحُبِّ فَانِي</td>
</tr>
<tr>
<td>لَمْ نَزَل طُول عُمْرِي نَعْشَقْ</td>
<td>وَالمَلِيحْ رِيتُو وَرَانِي</td>
</tr>
<tr>
<td>المَحَبَّة بِالمَعَانِي</td>
<td>وَالمِلاح مَا هُمْ سَوِيَّا</td>
</tr>
<tr>
<td>عَيْنِي وَقَلْبِي نَشبَانِي</td>
<td>هم الاثنَيْن اتفَقُوا عَلَيَّا</td>
</tr>
<tr><td colspan="2"><br></td></tr>
<tr><td colspan="2" class="text-bold text-italic">صنعة توشيح شغل</td></tr>
<tr><td colspan="2"><br></td></tr>
<tr>
<td>يَا مُسْلِمِينْ قَلْبِي دَعَانِي</td>
<td>آشْ كَانْ دَعَاهْ</td>
</tr>
<tr>
<td colspan="2">حَتَّى سَكَنْ فِيهِ الغَرَامْ</td>
</tr>
<tr>
<td>خَشْفُ الظِّبَا لَقَدْ رَمَانِي</td>
<td>طَرْفُ سَنَاهْ</td>
</tr>
<tr>
<td colspan="2">عَلَّ الفُؤَاد بِالسَّقَامْ</td>
</tr>
<tr>
<td>وَلا قَوِيت نَمْسَكْ عِنَانِي</td>
<td>حِينَ نَرَاهْ</td>
</tr>
<tr>
<td colspan="2">هَايَمْ مُهِيمْ بَيْنَ الأنَامْ</td>
</tr>
<tr>
<td colspan="2">فَهَلْ لِدَائِي مِنْ طَبِيبْ</td>
</tr>
<tr>
<td colspan="2">لَعَلَّهُ يُبْرِي العَلِيلْ</td>
</tr>
<tr>
<td>عَشِيقْ مُتَيَّمْ كَئِيبْ</td>
<td>بَيْنَ الأنَامْ</td>
</tr>
<tr>
<td colspan="2">صَبٌّ قَتِيلْ</td>
</tr>
<tr><td colspan="2"><br></td></tr>
<tr><td colspan="2" class="text-bold text-italic">صنعة توشيح</td></tr>
<tr><td colspan="2"><br></td></tr>
<tr>
<td>مَا الغَرَام إلا بَلِيَّا</td>
<td>فَاعْذِرُونِي يَا مُقَابِلْ</td>
</tr>
<tr>
<td>أشْعَلَتْ نَارُهُ قَوِيَّا</td>
<td>تَرَكَت الجِسْمَ نَحِيلْ</td>
</tr>
<tr>
<td>ضَيْفُنَا شِبْهُ الثَرَيَّا</td>
<td>حُبُّهُ فِي القَلْبِ حَاصِلْ</td>
</tr>
<tr>
<td colspan="2">شَفْرُهُ كَالسَّيْفِ يَقطَعْ</td>
</tr>
<tr>
<td colspan="2">بِالسِّهَامْ لَقَدْ رَمَانِي</td>
</tr>
<tr>
<td>بِالوِصَال لازِلت أطْمَعْ</td>
<td>طُولَ عُمْرِي وزَمَانِي</td>
</tr>
<tr><td colspan="2"><br></td></tr>
<tr><td colspan="2" class="text-bold text-italic">صنعة توشيح</td></tr>
<tr><td colspan="2"><br></td></tr>
<tr>
<td>مُذ سَقَى بَناتِ القَدِّ</td>
<td>بِمِياهِ الفَلَجْ</td>
</tr>
<tr>
<td>أوْقَدَتْ فِي دُجَى الجعْدِ</td>
<td>وَزعَفِ الدَّعَجْ</td>
</tr>
<tr>
<td>امْلا يَا فَارِجَ الهَمِّ</td>
<td>مِنْ قَهْوَةِ الكُرُومْ</td>
</tr>
<tr>
<td>اسْقِنَا بِنْتَ الكَرْمِ</td>
<td>تَحْتَ ضِيَا النُّجُومْ</td>
</tr>
<tr>
<td>فَيضْ الكَاسْ وَدَعْ لَوْمِي</td>
<td>يَا أخِي لا تَلُمْ</td>
</tr>
<tr>
<td>من صهيبه بَرَت جُهْدِي</td>
<td>مَعَ بَرْقِ اللَّعَجْ</td>
</tr>
<tr>
<td>والعِذَارُ علَى الخَدْ</td>
<td>نِلْت مِنْهُ حَرَجْ</td>
</tr>
<tr><td colspan="2"><br></td></tr>
<tr><td colspan="2" class="text-bold text-italic">صنعة - توشيح متقارب</td></tr>
<tr><td colspan="2"><br></td></tr>
<tr>
<td>أتَانِي زَمَانِي بمَا أرْتَضِي</td>
<td>فباللَّهِ يَا دَهْرُ لا تَنْقَضِي</td>
</tr>
<tr>
<td>ويَا ساعَةَ الوَصْلِ عُودِي لَنَا</td>
<td>لأنَّ الحبيبَ عَلَيْنَا رَضِي</td>
</tr>
<tr>
<td>سَقانِي بِكأسِ الوَفَا شُرْبَةً</td>
<td>فَشَاهَدْتُ فِي الكأسِ نُورُ يُضي</td>
</tr>
<tr>
<td>ونَحْنُ عَلَى العَهْدِ نَرْعَى الذِّمَامْ</td>
<td>وعَهْدُ المحبين لا يَنقضِي</td>
</tr>
<tr>
<td>صَدَدتَ فكنْتَ مَلِيحَ الصُّدُودْ</td>
<td>وأعْرَضْتَ أفْدِيكَ مِن مُعرِضِ</td>
</tr>
</table>

