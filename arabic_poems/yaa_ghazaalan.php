    <h1 class="text-centre">O gazelle that shelters me, how beautiful you are!</h1>
    <h2 class="text-centre">Yā ġazālan bil-ḥimā mā ajmalak</h2>

    <p class="text-centre text-italic">
    ﻧﻮﻉ ﺍﻟﺸﻌﺮ: غزل ﻋﻠﻰ ﺑﺤﺮ ﺍلكامل<br>
    ﺍﻟﺸﺎﻋﺮ: مجهول<br>
    منطقته: ﺍﻷﻧﺪﻟﺲ<br>
    ﻋﺼﺮ: ﻗﺮﻥ الثامن-الخامس عشر<br>
    </p>

    <p class="text-centre">
    يا غزالًا بالحِمى ما أجملَك<br>
    يا تُرى في قتلتي مَن حلّ لك<br>
    <br>
    كنتَ لا تغْفُلُ عني ساعًة<br>
    عَلَّمُوك الهجرَ حتى لَذَّ لك<br>
    <br>
    زارني طيفُ خيالٍ في الكرى<br>
    قلتُ يا طيف الخيال مَن أرسلك<br>
    <br>
    قال مَن تَعْشَقُه أرسلك<br>
    الذي بعد هواك أشغل لك<br>
    <br>
    قلتُ خَلَّ العشقِ لا تعبأَ به<br>
    قال لولا العشقُ ما دارَ الفَلَك<br>
    <br>
    قلتُ في العشقِ امتحانٌ للفتى<br>
    وعلى العُشّاقِ هانَ الهَلَك<br>
    <br>
    أنا مملوكٌ وحبي مالكي<br>
    ليس لي حُكْمٌ على ما قد مُلِك<br>
    </p>

    <p class="text-centre text-italic">
    Type of poem: Ġazal (poetry on love) in Kāmil metre<br>
    Name of poet: unknown<br>
    Region of poet: al-Andalus<br>
    Era: 8th-15th century<br>
    </p>

    <p class="text-centre">
    O gazelle that shelters me, how beautiful you are!<br>
    you see the very end of me<a href="#r1" class="a-reference">[1]</a>, who gave you my permission?<br>
    <br>
    You did not take your eyes off me, even in my sleep,<br>
    they taught you of estrangement, that drew you back to me<a href="#r2" class="a-reference">[2]</a><br>
    <br>
    I saw a ghostly shadow<a href="#r2" class="a-reference">[3]</a> appearing in my dreams<br>
    I said: O vision<a href="#r2" class="a-reference">[3]</a>, who has sent for me?<br>
    <br>
    It said: she whom that loves you has sent me<br>
    she, from whose love you are wasting away<br>
    <br>
    I said: the passion that plagues me cannot exhaust me!<br>
    It said: were it not for your love, the stars would go out<a href="#r4" class="a-reference">[4]</a><br>
    <br>
    I said: for a young man, in love there is a test<br>
    but for the lovers, easier is death<br>
    <br>
    I am but taken and my love is whom reigns me<br>
    and I no longer know whom has taken my heart<br>
    </p>

