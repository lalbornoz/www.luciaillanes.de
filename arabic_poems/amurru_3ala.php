<h1 class="text-centre">I passed by the houses, the houses of Laylā</h1>
<h2 class="text-centre">Amurru ʕāla d-diyāri diyāri laylā</h2>

<p class="text-centre text-italic">
نوع الشعر: فطعة<br>
الشاعر: قيس بن الملوح<br>
منطقته: نجد<br>
عصر: أموي<br>
</p>

<p class="text-centre">
أَمُرُّ عَلى الدِيارِ دِيارِ لَيلى<br>
أُقَبِّلَ ذا الجِدارَ وَذا الجِدارا<br>
<br><br>
وَما حُبُّ الدِيارِ شَغَفنَ قَلبي<br>
وَلَكِن حُبُّ مَن سَكَنَ الدِيارا<br>
</p>

<p class="text-centre text-italic">
Type of poem: Qiṭʕah<br>
Name of poet: Qays b. al-Mulawwaḥ<br>
Region of poet: Najd<br>
Era: Umayyad<br>
</p>

<p class="text-centre">
I passed by the houses<a href="#r1" class="a-reference">[1]</a>, the houses of Laylā<a href="#r2" class="a-reference">[2]</a><br>
I kissed this wall and I kissed that wall<br>
<br>
but it is not love of the houses that has enamoured my heart<br>
but love of those that dwell within<br>
</p>

