</p>

<p style="text-align: center">
نحن غرابا عك عك<br>
نحن غرابا عك عك<br>
<br>
عكٌّ إليك عانية<br>
عكٌّ إليك عانية<br>
<br>
عبادك اليمانية<br>
عبادك اليمانية<br>
<br>
