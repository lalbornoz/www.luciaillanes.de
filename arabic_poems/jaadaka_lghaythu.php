<h1 class="text-centre">Generous was the rain when it fell in vain</h1>
<h2 class="text-centre">Jādaka l-ġayṯu iḏa l-Ġayṯu hamā</h2>

<p class="text-centre text-italic">
ﻧﻮﻉ ﺍﻟﺸﻌﺮ: مقتطف من موشحة<br>
ﺍﻟﺸﺎﻋﺮ: أَبُو عَبْدِ اللهِ مُحَمَّدُ بْنْ عَبْدِ اللهِ بْنِ سَعِيدِ بْنِ عَبْدِ اللهِ بْنِ سَعِيدِ بْنِ عَلِي بْنِ أَحْمَدَ اَلسَّلْمَانِي اَلْخَطِيبِ المعروف بِلِسَانِ اَلدِّينِ ابْنِ اَلْخَطِيبِ والمُلقب بـ ذِي الْوِزَارَتَيْنِ وَذي اَلْعُمْرَيْنِ وَذُي اَلْمِيتَتَيْنِ<br>
منطقته: ﺍﻷﻧﺪﻟﺲ (غرناطة)<br>
ﻋﺼﺮ: ﻗﺮﻥ الثالث ﻋﺸﺮ<br>
</p>

