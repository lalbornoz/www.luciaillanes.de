        <br>
        صَفراءُ لا تَنزَلُ الأَحزانُ ساحَتَها<br>
        لَو مَسَّها حَجَرٌ مَسَّتهُ سَرّاءُ<br>
        <br>
        مِن كَفِّ ذاتِ حِرٍ في زِيِّ ذي ذَكَرٍ<br>
        لَها مُحِبّانِ لوطِيٌّ وَزَنّاءُ<br>
        <br>
        قامَت بِإِبريقِها وَاللَيلُ مُعتَكِرٌ<br>
