    <h1 class="text-centre">We blame our time but we are to blame</h1>
    <h2 class="text-centre">Naʕību zamāninā wal-ʕaybu fīnā</h1>

    <p class="text-centre text-italic">
    نوع الشعر: قطعة على بحر الوافر<br>
    الشاعر: أبو عبد الله محمد بن إدريس الشافعيّ المطَّلِبيّ القرشيّ<br>
    منطقته: أساسا: مكة والمدينة, اليمن, بغداد ومصر<br>
    عصر: عباسي - قرن الثامن-التاسع<br>
    </p>

    <p class="text-centre">
    نَعيبُ زَمانَنا وَالعَيبُ فينا<br>
    وَما لِزَمانِنا عَيبٌ سِوانا<br>
    <br>
    وَنَهجو ذا الزَمانِ بِغَيرِ ذَنبٍ<br>
    وَلَو نَطَقَ الزَمانُ لَنا هَجانا<br>
    <br>
    وَلَيسَ الذِئبُ يَأكُلُ لَحمَ ذِئبٍ<br>
    وَيَأكُلُ بَعضُنا بَعضاً عَيانا<br>
    </p>

    <p class="text-centre text-italic">
    Type of poem: Qiṭʕah in metre Wāfir<br>
    Name of poet: Abū ʕabdi-llāh Muḥammad b. Idrīs aš-Šāfiʕī al-Muṭṭalibī al-Qurašī<br>
    Region of poet: primarily: Makkah and al-Madīnah, the Yaman, Baġdād, and Egypt<br>
    Era: ʕabbāsid - 8th-9th century<br>
    </p>

    <p class="text-centre">
    We blame our time<a href="#r1" class="a-reference">[1]</a> but we are to blame<br>
    and no fault has our age except in ourselves<br>
    <br>
    We scold our days without reason to blame<br>
    and were it able to speak then us it would scold<br>
    <br>
    And not even the wolf feasts upon his own kind<br>
    when we prey upon another in broad daylight with no shame<br>
    </p>

