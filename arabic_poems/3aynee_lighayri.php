    <h1 class="text-centre">My eyes do not see except for thy beauty</h1>
    <h2 class="text-centre">
    ʕaynī li-ġayri jamālikum lā tanżuru<a href="#r1" class="a-reference">[1]</a>
    </h2>

    <p class="text-centre text-italic">
    ﻧﻮﻉ ﺍﻟﺸﻌﺮ: قصيدة ﻋﻠﻰ ﺑﺤﺮ الكامل<br>
    ﺍﻟﺸﺎﻋﺮ: عبد الغني بن إسماعيل بن عبد الغني بن إسماعيل بن أحمد بن إبراهيم النابلسي الدمشقي الحنفي النقشبندي القادري<br>
    منطقته: دمشق<br>
    ﻋﺼﺮ: الدولة العثمانية (القرن السابع عشر-الثامن عشر)<br>
    </p>

    <p class="text-centre">
    عيني لغير جمالكم لا تنظر<br>
    وسواكم في خاطري لا يخطر<br>
    <br>
    صبرت قلبي عنكم فأجابني<br>
    لا صبر لي لا صبر لي كيف أصبر<br>
    <br>
    لا اصبر حتى يراكم ناظري<br>
    وعلى محبتكم أموت وأحشر<br>
    <br>
    غبتم وغابت راحتي من بعدكم<br>
    والعيش صار من الجفا يتكدر<br>
    <br>
    لا فرق ما بيني وبين خيالكم<br>
    إن غاب غبتم أو حضرتم أحضر<br>
    <br>
    اثنان نحن وفي الحقيقة واحد<br>
    لكن أنا الأدنى وأنتم الأكبر<br>
    </p>

    <p class="text-centre text-italic">
    Type of poem: Qaṣīdah in metre Kāmil<br>
    Name of poet: ʕabdu l-Ġanī b. Ismāʕīl b. ʕabdi l-Ġanī b. Ismāʕīl b. Aḥmad b. Ibrāhīm b. an-Nāblusī ad-Dimašqī al-Ḥanafī an-Naqšabandī al-Qādirī<br>
    Region of poet: Damascus<br>
    Era: Ottoman Empire (17-18th century)<br>
    </p>

    <p class="text-centre">
    My eyes do not see except for thy beauty<a href="#r1" class="a-reference">[1]</a><br>
    and the thought of but you does not cross my mind<br>
    <br>
    I asked my heart to be patient for you but it said:<br>
    no patience, no patience have I and how could I bear?<br>
    <br>
    I shall not be patient 'til your eye meets my eye<br>
    and sees your beloved. I die and I perish, destroyed am I<br>
    <br>
    when absent you are and my comfort is far, with no consolation,<br>
    and my life turns full of sorrow from your harsh, cruel shunning<br>
    <br>
    Between I and thy spirit there is no separation<br>
    whether you vanish in absence or appear in my presence<br>
    <br>
    Two we may be but in truth we are one<br>
    but I am but lowly<a href="#r2" class="a-reference">[2]</a>, the highest and greatest is you<br>
    </p>

