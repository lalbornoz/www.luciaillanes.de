    <h1 class="text-centre"></h1>
    <h2 class="text-centre">Iḏā l-Marʔu lam yudnas mina l-luʔmi ʕirḍuhu</h1>

    <p class="text-centre text-italic">
    نوع الشعر: قصيدة على بحر الطويل<br>
    الشاعر: السموأل بن غريض بن عادياء الأزدي<br>
    منطقته: الحجاز<br>
    عصر: جاهلي - قرن السادس<br>
    </p>

    <p class="text-centre">

    إِذا المَرءُ لَم يُدنَس مِنَ اللُؤمِ عِرضُهُ<br>
    فَكُلُّ رِداءٍ يَرتَديهِ جَميلُ<br>
    <br>
    وَإِن هُوَ لَم يَحمِل عَلى النَفسِ ضَيمَها<br>
    فَلَيسَ إِلى حُسنِ الثَناءِ سَبيلُ<br>
    <br>
    تُعَيِّرُنا أَنّا قَليلٌ عَديدُنا<br>
    فَقُلتُ لَها إِنَّ الكِرامَ قَليلُ<br>
    <br>
    وَما قَلَّ مَن كانَت بَقاياهُ مِثلَنا<br>
    شَبابٌ تَسامى لِلعُلى وَكُهولُ<br>
    <br>
    وَما ضَرَّنا أَنّا قَليلٌ وَجارُنا<br>
    عَزيزٌ وَجارُ الأَكثَرينَ ذَليلُ<br>
    <br>
    لَنا جَبَلٌ يَحتَلُّهُ مَن نُجيرُهُ<br>
    مَنيعٌ يَرُدُّ الطَرفَ وَهُوَ كَليلُ<br>
    <br>
    رَسا أَصلُهُ تَحتَ الثَرى وَسَما بِهِ<br>
    إِلى النَجمِ فَرعٌ لا يُنالُ طَويلُ<br>
    <br>
    هُوَ الأَبلَقُ الفَردُ الَّذي شاعَ ذِكرُهُ<br>
    يَعِزُّ عَلى مَن رامَهُ وَيَطولُ<br>
    <br>
    وَإِنّا لَقَومٌ لا نَرى القَتلَ سُبَّةً<br>
    إِذا ما رَأَتهُ عامِرٌ وَسَلولُ<br>
    <br>
    يُقَرِّبُ حُبُّ المَوتِ آجالَنا لَنا<br>
    وَتَكرَهُهُ آجالُهُم فَتَطولُ<br>
    <br>
    وَما ماتَ مِنّا سَيِّدٌ حَتفَ أَنفِهِ<br>
    وَلا طُلَّ مِنّا حَيثُ كانَ قَتيلُ<br>
    <br>
    تَسيلُ عَلى حَدِّ الظُباتِ نُفوسُنا<br>
    وَلَيسَت عَلى غَيرِ الظُباتِ تَسيلُ<br>
    <br>
    صَفَونا فَلَم نَكدُر وَأَخلَصَ سِرَّنا<br>
    إِناثٌ أَطابَت حَملَنا وَفُحولُ<br>
    <br>
    فَنَحنُ كَماءِ المُزنِ ما في نِصابِنا<br>
    كَهامٌ وَلا فينا يُعَدُّ بَخيلُ<br>
    <br>
    وَنُنكِرُ إِن شِئنا عَلى الناسِ قَولَهُم<br>
    وَلا يُنكِرونَ القَولَ حينَ نَقولُ<br>
    <br>
    إِذا سَيِّدٌ مِنّا خَلا قامَ سَيِّدٌ<br>
    قَؤُولٌ لِما قالَ الكِرامُ فَعُولُ<br>
    <br>
    وَما أُخمِدَت نارٌ لَنا دونَ طارِقٍ<br>
    وَلا ذَمَّنا في النازِلينَ نَزيلُ<br>
    <br>
    وَأَيّامُنا مَشهورَةٌ في عَدُوِّنا<br>
    لَها غُرَرٌ مَعلومَةٌ وَحُجولُ<br>
    <br>
    وَأَسيافُنا في كُلِّ شَرقٍ وَمَغرِبٍ<br>
    بِها مِن قِراعِ الدارِعينَ فُلولُ<br>
    <br>
    مُعَوَّدَةٌ أَلّا تُسَلَّ نِصالُها<br>
    فَتُغمَدَ حَتّى يُستَباحَ قَبيلُ<br>
    <br>
    سَلي إِن جَهِلتِ الناسَ عَنّا وَعَنهُمُ<br>
    فَلَيسَ سَواءً عالِمٌ وَجَهولُ<br>
    </p>

    <p class="text-centre text-italic">
    Type of poem: Qaṣīdah in metre Ṭawīl<br>
    Name of poet: as-Samawʔal b. Ġarīḍ b. ʕādiyāʔ al-Azdī<a href="#r1" class="a-reference">[1]</a><br>
    Region of poet: Ḥijāz<br>
    Era: Pre-Islāmic - 6th century<br>
    </p>

    <p class="text-centre">
    When a man does not stain with wickedness his honour<a href="#r2" class="a-reference">[2]</a><br>
    then every robe that he dons upon him is pleasing<a href="#r3" class="a-reference">[3]</a><br>
    <br>
    And if he cannot bear the injustice<a href="#r4" class="a-reference">[4]</a> he suffers<br>
    no way is there for him to the benefit of praise<a href="#r5" class="a-reference">[5]</a><br>
    <br>
    She<a href="#r6" class="a-reference">[6]</a> denounced us as so few we are in number<br>
    so I said to them: "The virtuous are always few in number<br>
    <br>
    not few are who remain to us equal in standing<br>
    young men that aspire to excel and also old men<br>
    <br>
    and hurt we are not by fewness of number, when our allies<br>
    are strong, when those of most are servile."<br>
    <br>
    Ours is the mountain, home to those whom we shelter,<br>
    strong and invincible, it repels the evil eye, even of the near-blind<br>
    <br>
    Its foundations are anchoured beneath the earth<a href="#r7" class="a-reference">[7]</a>, it rises above them<br>
    towards the stars, its roots beyond reach, and grows tall<br>
    <br>
    It is al-Ablaq<a href="#r8" class="a-reference">[8]</a>, the one and only, its reputation precedes it<br>
    it is precious to whom seeks it and lofty and tall<br>
    <br>
    And we are a people who see in being slain no disgrace<br>
    even though b. ʕāmir and b. Salūl see it otherwise<br>
    <br>


    </p>

