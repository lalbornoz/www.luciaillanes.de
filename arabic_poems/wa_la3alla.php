<h1 style="text-align: center">May all that you fear not turn out to pass</h1>
<h2 style="text-align: center">Wa-laʕalla mā taḫšāhu laysa bi-kāʔin</h2>

<p style="text-align: center; font-style: italic">
ﻧﻮﻉ ﺍﻟﺸﻌﺮ: قطعة ﻋﻠﻰ ﺑﺤﺮ الكامل<br>
ﺍﻟﺸﺎﻋﺮ: أبو إسحاق إسماعيل بن القاسم بن سويد العنزي المعروف بأبي العتاهبة<br>
منطقته: بغداد<br>
ﻋﺼﺮ: ﺍﻟﻌﺒﺎﺳﻲ ﺍﻷﻭﻝ<br>
</p>

<p style="text-align: center">
ولعل ما تخشاه ليس بكائن<br>
ولعل ما ترجوه سوف يكون<br>
ولعل ما هونت ليس بهين<br>
ولعل ما شدّدت سوف يهون<br>
</p>

<p style="text-align: center; font-style: italic">
Type of poem: Qiṭʕah in metre Kāmil<br>
Name of poet: Abū Isḥāq Ismāʕīl b. al-Qāsim b. Suwayd al-ʕanzī known as Abū l-ʕatāhiyyah</a><br>
Region of poet: Baġdād<br>
Era: Early ʕabbāsid<br>
</p>

<p style="text-align: center">
May all that you fear not turn out to pass<br>
May all that you hope for turn out to come<br>
Perhaps what you thought easy did not come with ease<br>
Perhaps what was once hard shall easy become<br>
</p>

