    <h1 class="text-centre">I said to Laylā: what is the remedy for my sleeplessness?</h1>
    <h2 class="text-centre">Qultu li-laylā mā dawāʔu s-sahr</h2>

    <p class="text-centre text-italic">
    نوع الشعر: غزل على بحر الرمل<br>
    الشاعر: أبو مدين الغوث<br>
    منطقه: الأندلس<br>
    عصر: المرابطي-الموحدي<br>
    </p>

    <p class="text-centre">
    قلت لليلى ما دواء السهر<br>
    قالت وصالي في طلوع السحر<br>
    <br>
    قلت وما أقصر وقت اللقا<br>
    قالت وما أعذب ذاك القصر<br>
    <br>
    قلت لقد أنهك جسمي الهوى<br>
    قالت شفاك الله عندي الخبر<br>
    <br>
    قلت ومَن ذلّكِ عن حالتي<br>
    قالت على الوجه يدل الأثر<br>
    <br>
    قلت لليلى كم أفنيت من عاشق<br>
    قالت وهل تعد أمواج البحار<br>
    </p>

    <p class="text-centre text-italic">
    Type of poem: Ġazal (poetry on love) in metre Ramal<br>
    Name of poet: Abū Madyan al-G̣awṯ<br>
    Region of poet: al-Andalus<br>
    Era: Almoravid-Almohad<br>
    </p>

    <p class="text-centre">
    I said to Laylā<a href="#r1" class="a-reference">[1]</a>: what is the remedy for my sleeplessness?<br>
    She said: our reunion by the coming of dawn<br>
    <br>
    I said to Laylā: how brief the times of our reunions are<br>
    She said: and how tormenting the briefness<br>
    <br>
    I said to Laylā: my body has become exhausted from our longing<br>
    She said: may God be your remedy for I have heard you<br>
    <br>
    I said to Laylā: and who has attested to my state?<br>
    She said: my face attests to your touch and trace<br>
    <br>
    I said to Laylā: how many have perished from your love?<br>
    She said: have you counted the waves of all the seas?<br>
    </p>

