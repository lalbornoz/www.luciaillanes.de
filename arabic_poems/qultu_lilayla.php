[1] Laylā of the tribe b. ʕāmir just as the poet, Qays b. al-Mulawwaḥ, known as "Majnūn" as in "crazy"
originating in his unfulfilled love for the former rendered beyond reach by, essentially, tribal
politics, both from the eponymous old traditional narrative known as "Layla and Majnun" of tremendous
popularity from the Maghrib to the Indian subcontinent and beyond, in great part also thanks to the
Persian poet Niẓāmī Ganjavī and equally so of great importance in Ṣūfi poetry.<br>
<br>
The dialogue constituting the poem recalls the poetic exchange between Majnūn and Laylā in Niẓāmī's
Leylī o Majnūn, wherein the two, in a secret meeting, recite poetry from a distance towards each other.<br>
