<h1 class="text-centre">If your love should cost me dearly then take me and my wealth</h1>
<h2 class="text-centre">In kān wiṣālak ʕalayya ġāli ḫuḏnī wa-mālī</h2>

<p class="text-centre text-italic">
ﻧﻮﻉ ﺍﻟﺸﻌﺮ: صنعة توشيح مع برولة تخليلة<br>
ﺍﻟﺸﺎﻋﺮ: ﻣﺠﻬﻮﻝ؛ ﻣﻮﺟﻮﺩ ﻓﻲ ﻣﻴﺰﺍﻥ ابطايحي ﻧﻮﺑﺔ رصد الذيبل<br>
