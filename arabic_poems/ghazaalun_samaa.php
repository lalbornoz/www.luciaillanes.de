<br>
له القدُّ يحكي لسمر القنا<br>
ﻭﺛﻐﺮٌ ﺷﻨﻴﺐ ﻭﺟﻴﺪ ﻭﺧﺎﻝ<br>
<br>
ﺳﺒﺎﻧﻲ ﻫﻮﺍﻩ ﻓﻘﻠﺖ ﻟﻪ<br>
ﺗﻠﻄّﻒْ ﻋﻠﻲّ ﺃﻣﻴﺮ ﺍﻟﺠﻤﺎﻝ<br>
<br>
ﻓﻬﻞ ﻟﻲ ﺑﻮﺻﻠﻚ ﻳﺎ ﺃﻣﻠﻲ<br>
