ﺍﻟﺸﺎﻋﺮ: ﻣﺠﻬﻮﻝ؛ ﻣﻮﺟﻮﺩ ﻓﻲ ﻣﻴﺰﺍﻥ ﺩﺭﺝ ﻧﻮﺑﺔ ﺍﻹﺳﺘﻬﻼﻝ<br>
