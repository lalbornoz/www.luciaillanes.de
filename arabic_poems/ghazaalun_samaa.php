</p>

<p class="text-centre">
ﻏﺰﺍﻝٌ ﺳﻤﺎ بالبها ﺧﻠﻘﺔً<br>
ﻭﻳﻔﻀﺢ ﺑﺪﺭَ ﺍﻟﺪﺟﻰ ﻓﻲ ﺍﻛﺘﻤﺎﻝ<br>
<br>
ﻭﻳﺰﺩﺍﺩ ﻟﻠﺸﻤﺲ ﻣﻦ ﺣﺴﻨﻪ<br>
