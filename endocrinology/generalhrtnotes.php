      <h2 class="text-centre" id="hrt-general-blood-tests">Blood tests pre-HRT; normal total T and high SHBG pre-HRT</h2>

      <p>
        When starting HRT, it is very highly recommended - in general - that extensive blood testing be done in order to determine a
        long list of serum levels, representing the pre-HRT state, and particularly including total E2 and total T as well as
        SHBG.<br>
        A consistently high SHBG pre-HRT that is not accounted for by diet, lifestyle, medication, etc. in conjunction with normal T
        as well as, most likely, barrage of pervasive, (adult) life-long physiological as well as mental symptoms, may be indicative
        of partial Androgen Receptor Insensitivity, as T is supposed to downregulate hepatic SHBG expression; this can be tested for.
      </p>

      <h2 class="text-centre" id="hrt-general-thyroid">Acute thyroid downregulation</h2>

      <p>
        Given sufficiently high Oestradiol target levels/gradients (e.g. 25 pg/mL adrenal E2 pre-HRT, &gt;=200 pg/mL E2 HRT, though
        this may very well apply at &gt;=150 pg/mL) initiation of HRT via any route of administration induces acute thyroid downregulation,
        presumably via TBG modulation. The degree of downregulation should be significant enough to be of significance, reflecting well
        established sexual dimorphism in, inter alia, metabolism, but not anywhere near pathological, even during or after a period or
        repeated periods of "hormonal chaos" (stable pre-HRT TSH ~1.0-1.2 µUI/L, stable post-HRT initiation (across multiple regimens)
        TSH ~1.5-1.8 µUI/L, during/after "hormonal chaos": ~2-2.3 µUI/L.)<br>
        This is normally unlikely to be a problem but certainly bears mentioning and documenting, particularly as diet/lifestyle changes
        may be recommendable.
      </p>

      <h2 class="text-centre" id="hrt-general-hypophysis">Familial pattern of hypophyseal dysregulation</h2>

      <p>
        A familial pattern of underlying systemic/dynamic hypophyseal dysregulation, possibly implicating the hypothalamus as well,
        expressing a diffuse range of genotypes and phenotypes may eventually express a genotype on the transsexual spectrum and potentially
        serve as a biomarker thereof. In the case of the author, the genotypes/phenotypes comprise:<br>

        <ol>
          <li>maternal uncle: hypophyseal tumour</li>
          <li>biological mother: moustache growth, androgynous facial anatomy, migraine, poor skin health, with an onset during puberty</li>
          <li>
            the author of this document (pre-HRT phenotype, all resolved post-HRT:)<br>
            <ol>
              <li>
                The hypothyroidic syndrome (metabolic/digestive slowing, fatigue/low energy levels, cold/heat intolerance, flu-like symptoms
                (implicating mucosal tissue, histamine, the immune system,) cognitive dysfunction/DPR, slow wound healing, skin dryness,
                insomnia, catecholamine dysregulation suspected but very hard to differentiate, etc.)
              </li>
              <li>
                A diffuse range of symptoms for which currently no phenomenologically and empirically valid notion exists and which must
                hence be provisionally subsumed under the terms of gender incongruence, gender dysphoria/dysmorphia, or transsexuality
                presenting with, DPR, cognitive, affective, social cognitive, as well as sympathetic ("screaming brain") as well as
                parasympathetic ANS dysfunction/dysregulation, psychoticity, non-normative gender/sex identity development and a much
                larger range of primarily physiological systemic and often subtle dysfunctionality/dysregulation that defies differentiation
                and specificity, in particular wrt. the above mentioned hypothyroidic syndrome.
              </li>
            </ol>
          </li>
        </ol>

        It bears emphasising that the dysregulation here most likely refers to decidedly non-linear phenomena that cannot be understood
        in a mechanistic manner and instead require a systemic approach that considers the hypothalamus-pituitary gland-... axes from a systems
        theoretical perspective and in terms of dynamic processes, implicating: timing, specific critical developmental windows, desynchronisation,
        homeostasis/balance, feedback, calibration, response curves, gain, set-points, receptor sensitivity/density, ratios of different receptor
        (sub)types, aberrant systemic reconfiguration in response to specific triggers (such as puberty onset,) etc.<br>
        <br>
        It additionally bears mentioning that this complicates anything relating to HRT owing to the possibility of introducing additional
        instability to a regulatory system that is already prone to instability by itself, plus downstream across and beyond merely the HPG
        axis, and appears to also increase susceptibility to certain concentration curves (e.g. transdermal vs. injections) that would
        otherwise not matter.
      </p>

