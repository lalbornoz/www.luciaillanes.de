      <h2 class="text-centre" id="hrt-general-interactions">Counterindications/adverse interactions</h2>

      <ul>
        <li>
          Any medication or substance in general which is metabolised by the enzymes <strong>Cytochrome P450 3A4 (CYP3A4)</strong>
          and/or <strong>Cytochrome P450 1A2 (CYP1A2)</strong> and of which, after taking into account bioavailability,
          average concentrations in serum in the µg/mL range are attained, such as <strong>Quetiapine IM and XR (Seroquel,
          etc.,)</strong> will interact with Oestradiol by significantly increasing serum levels relative to the same amount
          thereof if no Quetiapine were present and simultaneously, owing to <strong>CYP3A4</strong> upregulation, significantly
          lower levels of Quetiapine are also induced relative to a male endocrine phenotype.<br>
          This must be taken into account when titrating upwards or downwards either Oestradiol or the medication in question or
          both as well as when estimating target serum levels relative to any HRT dosage.<br>
          <br>
          This occurs both due to competition for the same enzyme by both Oestradiol and Quetiapine as well a difference in
          average concentrations of close to two orders of magnitude: in the case of Oestradiol, 100-200 pg/mL vs. in the case
          of Quetiapine, ~1-10 µg/mL given dosages of 100-350 mg p/d and an oral bioavailability of 9%.<br>
          While this phenomenon is acknowledged and discussed in literature<a href="#r1" class="a-reference">[1]</a>,
          it is not sufficiently well-known and particularly so in conjunction with HRT.
          <br>
          If transdermal Oestradiol gel is used and hence, twice daily, Quetiapine, when taken once daily and at night will
          additionally induce asymmetry in the morning Oestradiol curve vs. nocturnal Oestradiol curve such that the nocturnal
          curve's Cmax, Cmin, and Cmean will be significantly higher than those of the morning curve.<br>
          <br>
          The difference in serum levels appears to roughly correspond to <strong>15-20 pg/mL</strong> Cmean Oestradiol for
          each <strong>25 mg</strong> of Quetiapine, based on multiple data points given varying Quetiapine dosages.<br>
        </li>
        <li>
          Any medication or substance in general which significantly increases SHBG, be it in the short, if acutely,
          mid, or long term, given habitual use, is to be ideally avoided entirely or at least treated with great care,
          particularly when stability in HRT has not been attained, as this will affect Free Oestradiol and constitute
          significant endocrine disruption. This includes, most importantly, <strong>Alcohol</strong><a href="#r2" class="a-reference">[2]</a>,
          especially if acutely.
        </li>
      </ul>

      <h2 class="text-centre" id="hrt-general-cycles">Oestradiol cycles</h2>

      <p>
        It bears mentioning that exogenously driven Oestradiol 35-day cycles with distinguished pseudo-follicular, pseudo-ovulatory
        peak, pseudo-luteal, as well as PMS/PMDD phases exist, as observed by the author of this document and in the form of a pervasive
        pattern in a sufficiently large amount of anecdotal evidence on Reddit, etc. The mechanism behind this is unknown though it would
        appear likely that the culprit is to be found in the emergent dynamics resulting from interactions between Oestradiol levels/curves
        and (inter alia) Oestrogen Receptor (ER) populations (density) as well as their sensitivity and the housekeeping thereof (e.g. enzymes)
        within each cell, being as Oestradiol is responsible in conjunction with ERs for making more ERs, constituting an autoregulatory
        feedback loop exhibiting hysteresis within a larger intracellular regulatory network with considerable inertia, see, inter alia,
        <a href="#r3" class="a-reference">[3]</a>, <a href="#r4" class="a-reference">[4]</a>, and
        <a href="/<?php echo $inc_lang ?>/endocrinology/transdermalnotes#hrt-transdermal-discontinuation">Transdermal Oestradiol gel: temporary discontinuation</a>.<br>
        <br>
        As observed by the author of this document, there exists a constraint on rate of change that is lower than 200% given any
        average concentration, e.g. an increase from 50 pg/mL to 100 pg/mL (e.g. +50 pg/mL) is excessive and will dangerously impact
        cycle structure; being as the constraint in endogenous puberty is +~1-3 pg/mL per cycle, it is likely to be significantly
        lower than 200%.<br>
        <br>
        In case of sufficient, especially pervasive, damage to cycle structure and subsequent recovery of stable Oestradiol inputs wrt.
        average serum levels as well as concentration curves, cycles may either temporarily lose coherence (highly pathological) and/or
        degrade to a cycle length of 7 days (equally pathological) for, on average, at least 7 iterations (e.g.  7 weeks,) followed, most
        likely, by an increase to a cycle length of 14 days (pathological, but much less problematic) for up to 2 iterations (e.g. 4 weeks
        in total,) and then recovery, back to the original (non-pathological) cycle length of 35 days. In case of failure to recover stable
        Oestradiol inputs, this process will continue oscillating indefinitely and maladaptively entrain these erratic patterns and eventually
        go on to adversely affect downstream systems, such as the thyroid gland and require, as soon as possible,
        <a href="/<?php echo $inc_lang ?>/endocrinology/transdermalnotes#hrt-transdermal-discontinuation">Transdermal Oestradiol gel: temporary discontinuation</a>.
      </p>

